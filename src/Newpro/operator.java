package Newpro;

public class operator {

	public static void main(String[] args) {
		int a=10;
		int b=20;
		int c=50;
	
		
		//Arithmetic operator
		System.out.println(a+b);
		System.out.println(a-b);
		System.out.println(b*a);
		System.out.println(c/a);
		//Logical operator
		System.out.println(a>b&&a<c);//f and t =f
		System.out.println(a<b||a>c);//t and f=t
	}
}
