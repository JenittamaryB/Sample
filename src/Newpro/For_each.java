package Newpro;

public class For_each {
  public static void main(String[] args) {
		int array[]= {1,2,3,4,5};
		char arry[]={'a'};
		String arr[]={"jenitta_mary"};
		for(int i:array)
		{
			System.out.println(i);
		}
		for(char i:arry)
		{
			System.out.println(i);
		}
		for(String i:arr)
		{
			System.out.print(i);
		}
			
	}

}